"""
Tipos de dados
str - string - Textos
int - numeros inteiros
float - ponto flutuante/ números reais
bool - booleano - true/false
"""

print('Luiz', type('luiz')) # mostra a classe do que está entre parenteses
print('10', type('10'), type(int('10'))) # converte o valor que está atribuido (type(int(0))

#String: nome
print('Lucas', type('Lucas'))
# Int: idade
print(26, type(26))
# Float: saldo em conta
print(18.50, type(18.50))
# Bool: qualquer informação true/false
print(20 > 25, type(20>25))
